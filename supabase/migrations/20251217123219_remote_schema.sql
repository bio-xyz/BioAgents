CREATE OR REPLACE FUNCTION "public"."cleanup_old_states"("days_to_keep" integer DEFAULT 1) RETURNS integer
    LANGUAGE "plpgsql"
    AS $$
DECLARE
  deleted_count INTEGER;
BEGIN
  -- Delete states older than specified days that are not referenced by messages
  DELETE FROM states
  WHERE id IN (
    SELECT s.id
    FROM states s
    LEFT JOIN messages m ON m.state_id = s.id
    WHERE m.id IS NULL
    AND s.created_at < NOW() - INTERVAL '1 day' * days_to_keep
  );

  GET DIAGNOSTICS deleted_count = ROW_COUNT;
  RETURN deleted_count;
END;
$$;


ALTER FUNCTION "public"."cleanup_old_states"("days_to_keep" integer) OWNER TO "postgres";


COMMENT ON FUNCTION "public"."cleanup_old_states"("days_to_keep" integer) IS 'Removes orphaned states older than specified days to prevent database bloat';



CREATE OR REPLACE FUNCTION "public"."match_documents"("query_embedding" "public"."vector", "match_threshold" double precision DEFAULT 0.7, "match_count" integer DEFAULT 10) RETURNS TABLE("id" "uuid", "title" "text", "content" "text", "metadata" "jsonb", "similarity" double precision)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  RETURN QUERY
  SELECT
    d.id,
    d.title,
    d.content,
    d.metadata,
    1 - (d.embedding <=> query_embedding) as similarity
  FROM documents d
  WHERE 1 - (d.embedding <=> query_embedding) > match_threshold
  ORDER BY d.embedding <=> query_embedding
  LIMIT match_count;
END;
$$;


