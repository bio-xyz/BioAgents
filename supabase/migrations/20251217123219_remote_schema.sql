CREATE OR REPLACE FUNCTION "public"."notify_conversation_state_change"() RETURNS "trigger"
    LANGUAGE "plpgsql"
    AS $$
DECLARE
    conv_id UUID;
BEGIN
    -- Find the conversation_id that references this state
    SELECT id INTO conv_id
    FROM conversations
    WHERE conversation_state_id = NEW.id
    LIMIT 1;

    -- Only notify if we found a conversation
    -- NOTE: Only send IDs, not full values (pg_notify has 8KB limit)
    IF conv_id IS NOT NULL THEN
        PERFORM pg_notify('conversation_states', jsonb_build_object(
            'operation', TG_OP,
            'id', NEW.id,
            'conversation_id', conv_id,
            'updated_at', NEW.updated_at
        )::text);
    END IF;

    RETURN NEW;
END;
$$;


ALTER FUNCTION "public"."notify_conversation_state_change"() OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."notify_message_change"() RETURNS "trigger"
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  -- NOTE: Only send IDs and small fields (pg_notify has 8KB limit)
  -- content/clean_content can be very large, so exclude them
  PERFORM pg_notify('chat_messages', jsonb_build_object(
    'operation', TG_OP,
    'id', NEW.id,
    'conversation_id', NEW.conversation_id,
    'user_id', NEW.user_id,
    'question', NEW.question,
    'has_content', NEW.content IS NOT NULL,
    'content_length', COALESCE(LENGTH(NEW.content), 0),
    'response_time', NEW.response_time,
    'state_id', NEW.state_id,
    'created_at', NEW.created_at
  )::text);
  RETURN NEW;
END;
$$;


ALTER FUNCTION "public"."notify_message_change"() OWNER TO "postgres";


